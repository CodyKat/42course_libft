#include <unistd.h>

void	ft_putendl_fd(char *s, int fd)
{
	while (*s != '\0')
	{
		write(fd, s, 1);
		s++;
	}
	write(fd, "\n", 1);
}

int main()
{
	ft_putendl_fd("abcd", 1);
	ft_putendl_fd("cde", 1);
	return 0;
}
